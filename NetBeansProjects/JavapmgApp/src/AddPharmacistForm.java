     public boolean verifData(){
     
     if(jTextField_FName.getText().equals("") && jTextField_LName.equals("")|| jTextField_BirthDate.equals("")
             ||jTextField_Phone.equals("")||jTextArea_Address.equals("")
             ||jTextField_Username.equals("")
             ||String.valueOf(jPasswordField2.getPassword()).equals(""))
     { 
         JOptionPane.showMessageDialog(null, "One or More Field Are Empty");
     return false;
     }else if(!String.valueOf(jPasswordField2.getPassword()).equals(String.valueOf(jPasswordField3.getPassword()))){
        
         JOptionPane.showMessageDialog(null, "Incorrect Password");
     return false;
     }else{
     return true;
     }
     }
