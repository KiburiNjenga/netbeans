    public boolean isUsenameExist(String un){
        boolean uExist = false;
        Connection con = MyConnection.getConnetion();
         PreparedStatement ps;
         ResultSet rs;
         
        try {
            ps = con.prepareStatement("SELECT * FROM `pharmacist` WHERE `username`=?");
            ps.setString(1, jTextField_Username.getText());
            rs = ps.executeQuery();
            if(rs.next()){
               uExist = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginPharmacist.class.getName()).log(Level.SEVERE, null, ex);
        }
    return uExist;
    }
