    private void jButtonAddStock1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddStock1ActionPerformed
          if(!s.isDrugExist(jTextField_DrugName.getText())){
            String dname = jTextField_DrugName.getText();
            String category = jTextField_DrugCategory.getText();
            String description = jTextArea_DrugDescription.getText();
            String status = jTextField_DrugStatus.getText();
            String mdate = jTextField_MDate.getText();
            String edate = jTextField_EDate.getText();
            int hour = Integer.valueOf(jSpinner1.getValue().toString());
            String price = jTextField_Price.getText();
            String quantity = jTextField_Quantity.getText();
            s.insertUpdateDeletePharmacist('i',null,dname, category, description, status, mdate, edate, hour,price,quantity);
            MainForm.jLabel_SaCount.setText("Drugs  Available="+Integer.toString(MyFunction.countData("stock")));
            
            try{
                manageStockForm.jTable1.setModel(new DefaultTableModel(null,new Object []{"Id","Name","Category","Description","Status","Manufacture Date","Expiry Date","Time Received","Price","Quantity"}));
                s.fillStockJtable(manageStockForm.jTable1, null);
            }catch(Exception ex)
            {
            
            System.out.println(ex.getMessage());
            }
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Drug Already Exists");
        }
    }//GEN-LAST:event_jButtonAddStock1ActionPerformed

