public void login()
{
        char[] pword = txtpassword.getPassword();
        String pw = "";
        boolean p = true;
        for(int i = 0; i<pword.length; i++){
            pw += pword[i];
        }
        try {
            db = new database();
            Statement st = database.conn.createStatement();
            ResultSet rs = st.executeQuery("select * from user where password = '"+pw+"' "
                    + "and delegation='"+cbodelegation.getSelectedItem()+"'");
            while(rs.next())
            {
                if(rs.getString("username").equals(txtusername.getText()))
                {
                    p = true;
                    mainForm mainForm = new mainForm();
                    mainForm.setVisible(p);
                    this.dispose();
                    if (cbodelegation.getSelectedItem() == "Receptionist")
                    {
                    mainForm.hide1();
                    mainForm.hide2();
                    mainForm.hide4();
                    mainForm.hide5();
                    }
                    if (cbodelegation.getSelectedItem() == "Lab attendant")
                    {
                    mainForm.hide1();
                    mainForm.hide2();
                    mainForm.hide3();
                    mainForm.hide5();
                    }
                    if (cbodelegation.getSelectedItem() == "Cashier")
                    {
                    mainForm.hide1();
                    mainForm.hide2();
                    mainForm.hide4();
                    mainForm.hide3();
                    }
                    else
                    if (cbodelegation.getSelectedItem() == "Doctor")
                    {
                    mainForm.hide1();
                    mainForm.hide3();
                    mainForm.hide4();
                    mainForm.hide5();
                    }  
                }
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Login Error", JOptionPane.ERROR_MESSAGE);
        }
        
        if(p == false){
            JOptionPane.showMessageDialog(this, "Access Denied! Incorrect credentials", "Login Error", JOptionPane.ERROR_MESSAGE);
            txtusername.setText("");
            txtpassword.setText("");
        }
}
